DROP TABLE IF EXISTS users, requests, items, bookings, comments CASCADE;
CREATE TABLE IF NOT EXISTS users (
  user_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL UNIQUE
);


CREATE TABLE IF NOT EXISTS requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
  description VARCHAR(512) NOT NULL,
  requester_id BIGINT REFERENCES users (user_id),
  created TIMESTAMP WITHOUT TIME ZONE CHECK NOT NULL

);

CREATE TABLE IF NOT EXISTS items (
  item_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(512) NOT NULL,
  available BOOLEAN NOT NULL,
  owner_id BIGINT REFERENCES users (user_id),
  request_id BIGINT REFERENCES requests (id)
);

CREATE TABLE IF NOT EXISTS bookings (
  booking_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
  start_date TIMESTAMP WITHOUT TIME ZONE NOT NULL,
  end_date TIMESTAMP WITHOUT TIME ZONE CHECK (end_date > start_date) NOT NULL,
  item BIGINT REFERENCES items (item_id) NOT NULL,
  booker_id BIGINT REFERENCES users (user_id) NOT NULL,
  owner_id BIGINT REFERENCES users (user_id) NOT NULL,
  status varchar(50) CHECK (status IN ('WAITING', 'APPROVED', 'REJECTED', 'CANCELED', 'All', 'CURRENT',
  'PAST', 'FUTURE'))
);

CREATE TABLE IF NOT EXISTS comments (
  comment_id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL UNIQUE,
  text VARCHAR(512) NOT NULL,
  item_id BIGINT REFERENCES items (item_id),
  author_id BIGINT REFERENCES users (user_id),
  created TIMESTAMP WITHOUT TIME ZONE CHECK NOT NULL
);